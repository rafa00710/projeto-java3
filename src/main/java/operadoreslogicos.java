public class operadoreslogicos {
    public static void main(String[] args) {

        //&& e , || ou

        if(10==10 && 20<20){ // && e
            System.out.println("verdadeiro");

        }else {
            System.out.println("falso");
        }

        if(10==10 || 20<20){ // || ou
            System.out.println("verdadeiro");

        }else {
            System.out.println("falso");
        }

    }
}
